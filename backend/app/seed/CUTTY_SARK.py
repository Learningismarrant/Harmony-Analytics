async def seed_cutty_sark(db) -> None:
    """Seed idempotent pour le test CUTTY SARK T-IRT (60 paires forcées).

    Appelé depuis seed_tests_surveys.py.
    Ne fait rien si le catalogue existe déjà (safe to re-run).
    """
    from sqlalchemy import select
    from app.shared.models import TestCatalogue, Question

    stmt = select(TestCatalogue).where(TestCatalogue.name == "CUTTY SARK T-IRT")
    result = await db.execute(stmt)
    existing = result.scalar_one_or_none()
    if existing:
        print("  CUTTY SARK T-IRT déjà présent — skip")
        return

    catalogue = TestCatalogue(
        name="CUTTY SARK T-IRT",
        description=(
            "Assessment Big Five en choix forcé (T-IRT) — 60 paires IPIP-120 maritime. "
            "Modèle Thurstonian IRT (Brown & Maydeu-Olivares, 2011). "
            "Résistant à la désirabilité sociale. Durée estimée : 15-20 minutes."
        ),
        instructions=(
            "Pour chaque paire, choisissez l'affirmation qui vous correspond le mieux. "
            "Il n'y a pas de bonne ou mauvaise réponse."
        ),
        test_type="tirt",
        n_questions=60,
        max_score_per_question=1,
        is_active=True,
    )
    db.add(catalogue)
    await db.flush()

    order = 1
    for bloc in CUTTY_SARK:
        for paire in bloc:
            q = Question(
                test_id=catalogue.id,
                text=paire["pair_id"],
                question_type="forced_choice",
                options=[
                    {
                        "side": opt["side"],
                        "ipip_id": opt["ipip_id"],
                        "domain": opt["domain"],
                        "facet": opt["facet"],
                        "score_weight": opt["score_weight"],
                        "text": opt["text"],
                    }
                    for opt in paire["options"]
                ],
                trait=paire["combination"],
                correct_answer=None,
                reverse=False,
                order=order,
            )
            db.add(q)
            order += 1

    await db.commit()
    print(f"  ✓ CUTTY SARK T-IRT seedé : catalogue.id={catalogue.id}, {order - 1} paires")